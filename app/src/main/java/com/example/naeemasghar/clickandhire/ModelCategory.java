public class ModelCategory {
    public String catId="";
    public String catName="";
    public String catIcon="";

    public String getId() {
        return catId;
    }


    public void setId(String id) {
        this.catId = id;
    }

    public String getName() {
        return catName;
    }

    public void setName(String name) {
        this.catName = name;
    }
    public String getIcon() {
        return catIcon;
    }

    public void setIcon(String icon) {
        this.catIcon = icon;
    }
}
